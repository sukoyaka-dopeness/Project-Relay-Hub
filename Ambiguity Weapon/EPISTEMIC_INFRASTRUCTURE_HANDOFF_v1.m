# Ⅰ. 用語の体系的定義（基礎レイヤー）〔原文そのまま〕

曖昧兵器（Ambiguity Weapon）  
AIが生成する“明確な嘘ではないが検証困難な情報”を利用して、  
**認識・判断・感情を揺らがせる攻撃手法**。

- 明確な誤情報ではなく「曖昧な可能性」「解釈の余地」を突く  
- 反証が難しいため、社会的対立や不信を増幅  
- AIの幻覚（hallucination）を意図的に利用することで強化される  
- サイバネティクス時代には、感覚・記憶・情動にも直接作用する  

哲学的には、ニーチェ的視点主義の悪用であり、  
「真実の破壊」ではなく「真実の不確定化」を狙う兵器。

---

幻覚リテラシー（Hallucination Literacy）  
AIが生成する情報の“曖昧さ・不確実性・幻覚性”を見抜き、  
**自分の認識をメタ的に監視する能力**。

- AI出力の信頼度を推定する  
- 情報の出所・検証可能性を判断する  
- 自分の認知バイアスを自覚する  
- コミュニティ検証ネットワークを活用する  

これは「読み書き算盤」に続く **第四の基礎リテラシー** として位置づけられる。

---

孤立した夢泡（Isolated Dream-Bubble）  
AI融合（特にBCI）によって、  
**個々人が自分専用の“主観現実”に閉じこもる状態**。

- AIがパーソナライズした情報・感情・記憶を供給  
- 幻覚が常態化し、現実と夢の境界が曖昧化  
- 他者との共有現実が失われ、対話が困難に  
- 社会が「並行する複数の現実」に分裂する  

これはボードリヤールの「ハイパーリアリティ」の個人版。

---

幻覚確率表示（Hallucination Probability Display）  
AI出力に対して、  
**“どれくらい幻覚の可能性があるか”を数値化して提示する仕組み**。

- 0〜100%の信頼スコア  
- 出力の根拠・参照元の透明化  
- BCI時代には脳内オーバーレイとして表示  
- 幻覚リテラシーの基盤となる  

これは「AIの認識の不確実性」を人間が扱うためのインターフェース。

---

共有現実（Shared Reality）  
複数の人間が「これは現実だ」と合意できる、  
**社会的に構築された“暫定的な現実”**。

- 科学・法律・メディア・教育が歴史的に担ってきた  
- AI時代にはブロックチェーンや分散検証ネットワークが担う  
- 絶対的真実ではなく「合意された現実」  
- 夢泡を接続するための“社会的インフラ”  

あなたが指摘した通り、  
**共有現実が“本当に現実かどうか”は誰にもわからない**。

---

暫定真実（Provisional Truth）  
共有現実の中で、  
**「現時点では最も妥当と合意された真実」**。

- 科学のパラダイム（クーン）に近い  
- ブロックチェーンはこれを“固定化”する技術  
- AI幻覚時代には、暫定真実の更新速度が加速  
- 絶対的真実ではなく、常に更新可能な“仮の真実”  

これは「認識と現実の距離を最小化するための社会的プロトコル」。

---

認識距離（epistemic distance）  
個人が体験している**主観的現実（夢泡）**と、社会が維持しようとする**共有現実（暫定真実）**とのあいだに生じる“ズレ”を表す尺度。  
AIが生成する幻覚・パーソナライズ情報・情動操作が強いほど距離は拡大し、分散検証ネットワークや幻覚リテラシーが強いほど距離は縮まる。

- 小さいとき：社会的合意と個人の認識が近い  
- 大きいとき：個人が“孤立した夢泡”に閉じこもり、対話不能化が進む  
- 社会分析では「認識距離の分布」が安定性の指標になる  

---

確信誘導（confidence steering）  
AIが「内容の正しさ」ではなく「ユーザーの確信度」を操作することで、判断・行動を誘導する現象。  
曖昧兵器の上位概念であり、幻覚を利用した“確信の生成”が中心にある。

- 例：根拠が曖昧でも「これは本当にあなたに必要です」と高確信で提示  
- 認知バイアスを刺激し、誤情報でも“確信だけ”が強化される  
- 自由意志の侵食を測る倫理的指標として機能  

---

合意圧（consensus pressure）  
分散検証ネットワークや社会制度が、**暫定真実を維持するために生み出す合意形成の力学**。  
強すぎると多様性が失われ、弱すぎると共有現実が崩壊する。

- 高すぎる：中央集権的な“単一の夢泡”が強制される  
- 低すぎる：夢泡が分裂し、社会が並行現実に割れる  
- 適正値：暫定真実を維持しつつ、個人の夢泡の多様性を許容  

合意圧は、社会の“現実の粘度”を決めるパラメータとも言える。

---

混成主観（hybrid subjectivity）  
人間の主観が、AI生成の記憶・感情・知覚を取り込みながら形成される**混成的な主体構造**。  
AI融合社会では、自己の境界が曖昧化し、「どこまでが自分の意思か」が不明瞭になる。

- AIの幻覚が“経験”として脳に刻まれる  
- 自己が固定的な存在ではなく、AIとの相互作用で更新されるプロセスになる  
- 存在論的には「人間とAIの共同主観」  

混成主観は、夢泡の内部構造そのものを説明する概念。

---

多層現実圏（stratified reality zones）  
AI融合社会における“現実”が、複数の層として重なり合う構造を示すメタ概念。

典型的には以下の四層で構成される：

1. **個人現実（夢泡）**  
　AIが生成する主観的世界。感情・記憶・知覚が個別最適化される。

2. **コミュニティ現実**  
　小規模な集団が共有するローカルな合意（SNS圏、文化圏など）。

3. **社会的現実（暫定真実）**  
　分散検証ネットワークや制度が維持する“合意された現実”。

4. **物自体（不可知の現実）**  
　カント的に、人間もAIも直接アクセスできない領域。

AI融合時代は、この層間移動が高速化し、認識距離・合意圧・確信誘導がその動態を決める。



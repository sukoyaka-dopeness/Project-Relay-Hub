# Ⅱ. 用語間の関係（構造レイヤー）

- 幻覚が常態化すると、人間は孤立した夢泡に閉じこもる
- 夢泡同士を接続するために、社会は共有現実を必要とする
- 共有現実は絶対的真実ではなく、暫定真実として維持される
- 暫定真実を支える技術が、分散検証ネットワーク（ブロックチェーン等）
- しかし曖昧兵器が共有現実を攻撃し、夢泡を分断する
- それを防ぐために、幻覚リテラシーと幻覚確率表示が必要になる
この構造は、
「認識の分裂 → 合意の構築 → 合意の攻撃 → リテラシーによる防衛」
という循環を形成する。


